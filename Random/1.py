from numpy import random
import numpy as np
import matplotlib.pyplot as plt
import seaborn as sns
x = random.randint(100)
print(x)
x = random.rand()
print(x)
x = random.randint(100, size=(3, 5))
print(x)
y = random.rand(3, 5)
print(y)
x = random.choice([63, 57, 47, 89])
print(x)
